'''
Program :- Given n as input, 
            => Print "Pepsi" id the number is divisible by 3
            => print "Coke" if the number is divisible by 4

Author :- AmRiyaz

Last Modified :- April 2021
'''
n = 36
if(n % 3 == 0):
    print("Pepsi")
if(n % 4 == 0):
    print("Coke")


'''
Output : 
Pepsi
Coke
'''